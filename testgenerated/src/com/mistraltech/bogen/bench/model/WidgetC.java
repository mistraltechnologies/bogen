package com.mistraltech.bogen.bench.model;

public class WidgetC {
    private int i;
    private byte b;
    private char c;
    private short s;
    private float f;
    private double d;
    private String a;

    public WidgetC(int i, byte b, char c, short s, float f, double d, String a) {
        this.i = i;
        this.b = b;
        this.c = c;
        this.s = s;
        this.f = f;
        this.d = d;
        this.a = a;
    }

    public int getI() {
        return i;
    }

    public byte getB() {
        return b;
    }

    public char getC() {
        return c;
    }

    public short getS() {
        return s;
    }

    public float getF() {
        return f;
    }

    public double getD() {
        return d;
    }

    public String getA() {
        return a;
    }
}
